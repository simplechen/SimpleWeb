package me.laochen.vo;

public class WxUserInfoVO {
	private String displayName;//车主
	private String phoneNo;
	private String carNo;
	private String carFrameNo;
	private String drivingInfo;
	private String insureNo;
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public String getPhoneNo() {
		return phoneNo;
	}
	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}
	public String getCarNo() {
		return carNo;
	}
	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}
	public String getCarFrameNo() {
		return carFrameNo;
	}
	public void setCarFrameNo(String carFrameNo) {
		this.carFrameNo = carFrameNo;
	}
	public String getDrivingInfo() {
		return drivingInfo;
	}
	public void setDrivingInfo(String drivingInfo) {
		this.drivingInfo = drivingInfo;
	}
	public String getInsureNo() {
		return insureNo;
	}
	public void setInsureNo(String insureNo) {
		this.insureNo = insureNo;
	}
	
}
